print("Diangram Batang Horizontal Jumlah Mahasiswa Laki Laki dan Perempuan PTIK UNS")

#Input
MahasiswaLakiLaki= int(input("Masukan Jumlah Siswa Laki Laki= "))
MahasiswaPerempuan= int(input("Masukan Jumlah Siswa Perempuan= "))

#Output
OutputMahasiswaLakiLaki= int(MahasiswaLakiLaki//10)
OutputMahasiswaPerempuan= int(MahasiswaPerempuan//10)

print("Laki Laki =", OutputMahasiswaLakiLaki*"*", MahasiswaLakiLaki)
print("Perempuan =", OutputMahasiswaPerempuan*"*", MahasiswaPerempuan)
